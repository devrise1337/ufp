// Counter animation for followers
function animateCounter() {
    const counter = document.querySelector('.followers-count');
    if (counter) {
        const target = 1055;
        let current = 0;
        const increment = target / 100;
        
        const timer = setInterval(() => {
            current += increment;
            if (current >= target) {
                current = target;
                clearInterval(timer);
            }
            counter.innerHTML = `<i class="fas fa-users"></i> +${Math.floor(current)} followers nous suivent d√©j√† !`;
        }, 20);
    }
}

// Initialize counter animation when section is visible
const counterObserver = new IntersectionObserver(function(entries) {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            animateCounter();
            counterObserver.unobserve(entry.target);
        }
    });
});

